/* *** 시퀀스 (SEQUENCE) ***
SEQUENCE : DB에서 제공하는 자동채번 객체
생성 : CREATE SEQUENCE 시퀀스 명;
삭제 : DROP SEQUENCE 시퀀스 명;

시퀀스명.NEXTVAL : 시퀀스 번호 (값) 생성
시퀀스명.CURRVAL : 현재 시퀀스 값 확인 (NEXTVAL 한번 이상 실행 후 사용 가능)

************************************/

CREATE SEQUENCE SEQUENCE1
MINVALUE 1 MAXVALUE 99999999999999999999999999999999999
INCREMENT BY 1
START WITH 1 CACHE 20 NOORDER NOCYCLE;
