-- BOOK 테이블에 INSERT 작업, BOOKID 최대값 + 1 
INSERT INTO BOOK
        (BOOKID, BOOKNAME, PUBLISHER, PRICE)
VALUES ((SELECT NVL(MAX(BOOKID),0) + 1 FROM BOOK),
        'MAX + 1적용', 'ITBOOK', 2000);
SELECT * FROM BOOK ORDER BY BOOKID DESC;
-----------------------------------------
-- 시퀀스 사용 (시작 번호 50, NOCACHE)
CREATE SEQUENCE SEQ_BOOK
START WITH 50 
INCREMENT BY 1 -- 생략가능 (DEFAULT 1)
NOCACHE;
------------------
-- BOOK 테이블에 시퀀스 사용해서 데이터 입력 처리
INSERT INTO BOOK    
        (BOOKID, BOOKNAME, PUBLISHER, PRICE)
VALUES (SEQ_BOOK.NEXTVAL, '시퀀스사용', 'IT_BOOK', 25000);
SELECT * FROM BOOK ORDER BY BOOKID DESC;

